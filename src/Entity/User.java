package Entity;

public class User {
    private static String uname;
    private static String upwd;
    public String getUname() {
        return uname;
    }
    public void setUname(String uName) {
        uname = uName;
    }

    public String getUpwd() {
        return upwd;
    }
    public void setUpwd(String uPwd) {
        upwd = uPwd;
    }
}
